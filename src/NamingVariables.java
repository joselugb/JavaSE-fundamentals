            //Upper Camel Case
public class NamingVariables {
    public static void main(String[] args) {
        int cellPhone = 992000219;
        System.out.println("El dato es: " + cellPhone);

        String $countryName = "Ecuador";
        System.out.println("El dato es: " + $countryName);
        String _backgroundColor = "light blue";
        System.out.println("El dato es: " + _backgroundColor);

        String currency$ = "USD";
        System.out.println("El dato es: " + currency$);
        String backgroundColor_ = "light red";
        System.out.println("El dato es: " + backgroundColor_);

        final int POSITION = -5; System.out.println("El dato es: " + POSITION);
        final int MAX_WIDTH = 9999; System.out.println("El dato es: " + MAX_WIDTH);
        final int MIN_WIDTH = 1; System.out.println("El dato es: " + MIN_WIDTH);

        //Lower Camel Case
        String fullName = "JOSE GAVILANES";
        int tamanioEnCentimetros = 50;
    }
}
